package operator;

public class Increment {      // ## Increment and Decrement 


	public static void main(String[] args) {
	
		 int a = 5;
	        a++;      // increment the value of num by 1
	        System.out.println("The value of num after incrementing is: " + a);
	        
	        int b = 5;
	        b--;      // decrement the value of num by 1
	        System.out.println("The value of num after Dectrment is: " + b );
	}

}

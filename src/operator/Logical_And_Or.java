package operator;

public class Logical_And_Or {           // ## Logical And (&&) Operator // ## Logical OR( || ) Operator


	public static void main(String[] args) {
		int a=5;
		int b=10;
		System.out.println(a<b && b!=a);  // in && operator both condition must True
		System.out.println(a>b || b!=a);  //in OR operator One Condition must True 
	        }

	}
